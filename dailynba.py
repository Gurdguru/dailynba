def remove_char(message):
    useless = ['-', '.', ',', ':', ';', '!', '/', '=', '?', '\\', '\'']
    for character in useless:
        message = message.replace(character, '')
    return message.lower()

def cap(message):
    message = f'{message[0].upper()}{message[1:].lower()}'
    return message

